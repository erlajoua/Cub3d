#include "../headers/cub.h"

void	free_map(t_cub *cub)
{
	int i;

	i = 0;
	while (i < cub->parse.nbline + 1)
	{
		free(cub->parse.map[i]);
		i++;
	}
	free(cub->parse.map);
}
